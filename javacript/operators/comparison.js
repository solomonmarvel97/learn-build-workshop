// > greater than
// < less than
// <= less than or equals
// >= greater than or equals
// == equal (value)
// === equal (value and type)

let a = 10
let b = "10"

// greater than
let gt = a > b
console.log(gt)

// lesser than
let lt = a < b
console.log(lt)

// lesser than or equals
let lte = a <= b
console.log(lte)

// greater than or equals
let gte = a >= b
console.log(gte)

// greater than or equals
let equals = a == b
console.log(equals)

// greater than or equals (with type)
let equalsWithType = a === b
console.log(equalsWithType)